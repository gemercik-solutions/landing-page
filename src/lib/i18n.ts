export type TranslationKey = keyof typeof translations.id;
export type Language = "id" | "en";

// Helper function to get translation
export function t(key: TranslationKey, lang: Language = "id"): string {
  return translations[lang][key] || translations.id[key] || key;
}

// Update language in DOM
export function updateLanguage(lang: Language) {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n") as TranslationKey;
    if (key && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
}

// Initialize language on page load
export function initLanguage() {
  const savedLang = (localStorage.getItem("language") || "id") as Language;
  if (savedLang === "en") {
    updateLanguage("en");
  }

  // Listen for language changes
  window.addEventListener("languageChanged", ((event: CustomEvent) => {
    updateLanguage(event.detail.language as Language);
  }) as EventListener);
}
